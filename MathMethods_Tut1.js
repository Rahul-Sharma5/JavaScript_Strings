//! cril(x) gives Nearest Number
var a = Math.ceil(5.2);
console.log(a);


//! floor(x) Gives Downwards Number
var b = Math.floor(5.2)
console.log(b);

//! round(x) Gives Upper Values
var c = Math.round(0.658);
console.log(c);

//! trunc(x) Gives Only Integer Number
var d = Math.trunc(8.19);
console.log(d);

//! max() Gives Max Number
var e = Math.max(9,5,6,2,4,7);
console.log(e);

//! min() Gives min Number
var f = Math.min(9,5,6,2,4,7);
console.log(f);

//! sqrt() Gives SquareRoot Number
var g = Math.sqrt(64);
console.log(g);

//! cbrt() Gives SquareRoot Number
var h = Math.sqrt(9);
console.log(h);

//! pow() Gives PowerOff Number (2*2*2)
var i = Math.pow(2,3);
console.log(i);

//! random() Gives Randow Number
var j = Math.random(); // or Math.floor(Math.random() * 10) + 1 ;
console.log(j);

//! abs() Gives absulate Value
var k = Math.abs(5.25);
console.log(k);


//! pi() Gives Constant Value
var l = Math.PI;
console.log(l);