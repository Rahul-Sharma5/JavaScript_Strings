//! indexOf() or lastindexOf() Methods in Strings


//! indexOf()
var str = "JavaScript is a Great Language for Students";
var a = str.indexOf("Great");
console.log(a);


//! lastindexOf()
var b = str.lastIndexOf("for");
console.log(b);

//! replace()
var c = str.replace("JavaScript", "Java");
console.log(c);

//! trim() remove extra spaces
var str2 = "            JavaScript        ";
var d = str2.trim();
console.log(d);


